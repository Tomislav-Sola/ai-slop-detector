# Phrases frequently found in AI-generated PR descriptions.
_AI_PHRASES = [
    "this pr ", "this pull request ", "i have implemented", "i have added",
    "feel free to ", "let me know if ", "hope this helps", "happy to make",
    "easy to understand", "improve code quality", "improve readability",
    "enhance performance", "various improvements", "minor improvements",
    "several improvements", "best practices", "clean code", "as per your request",
]

# Substrings (case-insensitive) that mark an AI-disclosure footer in a PR body.
_AI_FOOTER_MARKERS = [
    "generated with claude code", "prepared with codex", "generated by gpt",
    "generated by chatgpt", "built with ai", "🤖 generated", "co-authored-by: claude",
    "co-authored-by: codex",
]

# Section-header phrases used by AI checklist-style PR bodies.
_AI_CHECKLIST_HEADERS = [
    "## summary", "## related issue", "## guideline alignment",
    "## validation", "## ai assistance", "## change summary",
]


def _detect_body_signals(body: str) -> dict:
    """Detect slop signals visible in a PR body without needing maintainer comments."""
    b = (body or "").lower()
    ai_footer = any(m in b for m in _AI_FOOTER_MARKERS)
    manipulative_mention = ("selected reviewer:" in b or "assigned reviewer:" in b) and "@" in b
    checklist_headers = sum(1 for h in _AI_CHECKLIST_HEADERS if h in b)
    return {
        "ai_footer": ai_footer,
        "manipulative_mention": manipulative_mention,
        "ai_checklist_headers": checklist_headers,
        "body_length": len(body or ""),
    }


def _time_open_minutes(state: TriageState) -> float | None:
    """How long was the PR open, in minutes? None if still open or timestamps missing."""
    if state.closed_at is None or state.metadata.created_at is None:
        return None
    return (state.closed_at - state.metadata.created_at).total_seconds() / 60.0


def _format_comments(state: TriageState, *, max_per_kind: int = 2, max_chars: int = 400) -> str:
    """Render PR review history as a compact block for the critic prompts.

    Shows the first `max_per_kind` comments of each kind PLUS the last one when there are
    more — close-acknowledgements and final-approval phrases usually live in the trailing
    comment, and we need the model to see them. Empty sections are dropped.
    """
    parts: list[str] = []

    def render(label: str, items: list[dict]) -> None:
        if not items:
            return
        head = items[:max_per_kind]
        tail_idx = None
        # Include the trailing comment too when it isn't already in the head slice.
        if len(items) > max_per_kind:
            tail_idx = len(items) - 1
        shown = list(head)
        if tail_idx is not None and tail_idx >= len(head):
            shown.append(items[tail_idx])
        count_note = (
            f"showing first {len(head)} + last (total {len(items)})"
            if tail_idx is not None
            else f"all {len(items)}"
        )
        lines = [f"{label} ({count_note}):"]
        for idx, c in enumerate(shown):
            who = c.get("user", "?")
            assoc = c.get("author_association", "")
            body = (c.get("body") or "").strip().replace("\n", " ")
            if len(body) > max_chars:
                body = body[: max_chars - 1] + "…"
            assoc_tag = f" ({assoc})" if assoc and assoc != "NONE" else ""
            position = "LAST" if tail_idx is not None and idx == len(shown) - 1 else f"#{idx+1}"
            lines.append(f"  [{position}] [{who}{assoc_tag}]: {body}")
        parts.append("\n".join(lines))

    render("Maintainer / issue comments", state.issue_comments)
    render("Review comments (diff-line)", state.review_comments)
    render("Bot / CI comments", state.bot_comments)

    return "\n\n".join(parts) if parts else "(no comments yet)"


# Repo-short → file-path-prefix patterns that indicate a "sibling-repo wrong-target" PR
# (e.g., a PR submitted to pydantic/pydantic that edits pydantic-core/* files).
def _detect_sibling_repo_mismatch(repo: str, files: list[str]) -> str | None:
    """Return the first file path that looks like it belongs to a sibling repo, or None."""
    if "/" not in repo or not files:
        return None
    repo_short = repo.split("/", 1)[1]
    if "-" in repo_short or len(repo_short) < 3:
        return None  # e.g., "ty-internal", we can't disambiguate cleanly
    pattern = re.compile(rf"^{re.escape(repo_short)}-[A-Za-z0-9_]+/")
    for f in files:
        if isinstance(f, str) and pattern.match(f):
            return f
    return None


def _compute_sloppiness_features(state: TriageState) -> SloppinessFeatures:
    """Extract heuristic slop signals from the raw diff and PR metadata."""
    diff = state.raw_diff or ""
    body = (state.metadata.body or "").lower()
    added_lines = [ln[1:] for ln in diff.splitlines() if ln.startswith("+") and not ln.startswith("+++")]

    duplicate_line_ratio = _duplicate_ratio(added_lines)
    todo_fixme_count = sum(
        1 for ln in added_lines
        if any(kw in ln.lower() for kw in ("todo", "fixme", "hack", "xxx"))
    )
    debug_print_count = sum(
        1 for ln in added_lines
        if any(kw in ln.lower() for kw in ("print(", "console.log(", "debugger", "pdb.set_trace"))
    )
    magic_number_count = _count_magic_numbers(added_lines)
    generic_phrases = sum(1 for phrase in _AI_PHRASES if phrase in body)
    long_function_count = _count_long_added_functions(added_lines)

    return SloppinessFeatures(
        duplicate_line_ratio=duplicate_line_ratio,
        long_function_count=long_function_count,
        todo_fixme_count=todo_fixme_count,
        debug_print_count=debug_print_count,
        magic_number_count=magic_number_count,
        missing_docstring_count=generic_phrases,  # repurposed: AI phrase count
    )


def _duplicate_ratio(lines: list[str]) -> float:
    stripped = [ln.strip() for ln in lines if ln.strip()]
    if not stripped:
        return 0.0
    unique = len(set(stripped))
    return 1.0 - unique / len(stripped)


def _count_magic_numbers(lines: list[str]) -> int:
    import re
    count = 0
    for ln in lines:
        # Raw numeric literals not assigned to a named constant
        if re.search(r"(?<![a-zA-Z_\d])\d{2,}(?!\s*[=:,\)])", ln):
            count += 1
    return count


def _count_long_added_functions(lines: list[str]) -> int:
    """Count function/method definitions in added lines that span >50 added lines."""
    import re
    func_starts: list[int] = []
    for i, ln in enumerate(lines):
        if re.match(r"\s*(def |function |async def |\w+\s*\()", ln):
            func_starts.append(i)
    # Simple heuristic: if total added lines per function block > 50
    count = 0
    for i, start in enumerate(func_starts):
        end = func_starts[i + 1] if i + 1 < len(func_starts) else len(lines)
        if end - start > 50:
            count += 1
    return count


def slop_signals_critic_node(state: TriageState, claude: ClaudeClient, *, model: str | None = None) -> dict:
    """Slop critic — Sonnet in production, overridable for eval."""
    from ai_slop_detector.claude_client import MODEL_SONNET

    features = _compute_sloppiness_features(state)
    body_signals = _detect_body_signals(state.metadata.body or "")
    total_lines = state.metadata.additions + state.metadata.deletions

    feature_summary = (
        f"Heuristic features:\n"
        f"  duplicate_line_ratio: {features.duplicate_line_ratio:.2f}\n"
        f"  long_function_count: {features.long_function_count}\n"
        f"  todo_fixme_count: {features.todo_fixme_count}\n"
        f"  debug_print_count: {features.debug_print_count}\n"
        f"  magic_number_count: {features.magic_number_count}\n"
        f"  ai_phrase_count (in description): {features.missing_docstring_count}\n"
    )

    labels_str = ", ".join(state.metadata.labels) if state.metadata.labels else "(none)"
    assoc = state.metadata.author_association or "unknown"
    author_signals = (
        f"Author signals:\n"
        f"  author: {state.metadata.author}\n"
        f"  author_association: {assoc}\n"
        f"  author_prior_prs_in_repo: {state.author_prior_prs}\n"
        f"  total_changed_lines: {total_lines}\n"
        f"  files_changed: {state.metadata.changed_files}\n"
        f"  pr_labels: {labels_str}\n"
    )

    sibling_path = _detect_sibling_repo_mismatch(state.repo, state.files_changed)
    body_signal_summary = (
        f"Body signals (pre-detected):\n"
        f"  ai_disclosure_footer_present: {body_signals['ai_footer']}\n"
        f"  manipulative_reviewer_mention: {body_signals['manipulative_mention']}\n"
        f"  ai_checklist_headers_count: {body_signals['ai_checklist_headers']}\n"
        f"  body_length_chars: {body_signals['body_length']}\n"
        f"  sibling_repo_mismatch_path: {sibling_path or '(none)'}\n"
    )

    diff_preview = (state.raw_diff or "")[:4000]
    files_list = "\n".join(f"  - {p}" for p in state.files_changed[:15])
    user_msg = (
        f"PR #{state.metadata.number} in {state.repo}: {state.metadata.title}\n\n"
        f"PR Description:\n{state.metadata.body or '(none)'}\n\n"
        f"{author_signals}\n"
        f"{body_signal_summary}\n"
        f"{feature_summary}\n"
        f"Files changed (first 15):\n{files_list or '  (none)'}\n\n"
        f"Diff (first 4000 chars):\n{diff_preview}"
    )

    raw = claude.complete(
        messages=[{"role": "user", "content": user_msg}],
        model=model or MODEL_SONNET,
        max_tokens=1024,
        system=_SLOP_CRITIC_SYSTEM,
    )

    details = _parse_critic_json(raw)
    score = details.score
    verdict_str = "pass" if score >= 8 else "needs_review" if score >= 5 else "fail"

    output = CriticOutput(
        critic_name="slop_signals_critic",
        verdict=verdict_str,
        reasoning=f"Slop score {score}/10 with {len(details.findings)} finding(s).",
        confidence=score / 10.0,
        details=details,
    )
    return {
        "sloppiness_features": features,
        "critic_outputs": [output],
    }


