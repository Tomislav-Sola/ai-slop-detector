def guidelines_critic_node(state: TriageState, claude: ClaudeClient, *, model: str | None = None) -> dict:
    """Guidelines critic — Sonnet in production, overridable for eval."""
    from ai_slop_detector.claude_client import MODEL_SONNET

    chunks_text = "\n\n".join(state.rag_chunks) if state.rag_chunks else "(no guideline context retrieved)"
    diff_preview = (state.raw_diff or "")[:6000]

    user_msg = (
        f"PR #{state.metadata.number}: {state.metadata.title}\n\n"
        f"PR Description:\n{state.metadata.body or '(none)'}\n\n"
        f"Diff (first 6000 chars):\n{diff_preview}\n\n"
        f"Retrieved guideline context:\n{chunks_text}"
    )

    raw = claude.complete(
        messages=[{"role": "user", "content": user_msg}],
        model=model or MODEL_SONNET,
        max_tokens=2048,
        system=_CRITIC_SYSTEM,
    )

    details = _parse_critic_json(raw)
    score = details.score
    verdict_str = "pass" if score >= 8 else "needs_review" if score >= 5 else "fail"

    output = CriticOutput(
        critic_name="guidelines_critic",
        verdict=verdict_str,
        reasoning=f"Score {score}/10 with {len(details.findings)} finding(s).",
        confidence=score / 10.0,
        details=details,
    )
    return {"critic_outputs": [output]}


def _parse_critic_json(raw: str) -> GuidelinesCriticOutput:
    """Extract and parse the JSON block the critic is expected to return.

    Handles the four observed failure modes:
    - Markdown fences around the JSON (despite being told not to).
    - Prose before or after the JSON block ("Extra data" error).
    - Invalid backslash escapes inside evidence strings.
    - Citations returned as list-of-dicts instead of list-of-strings.
    """
    data = _extract_json(raw)
    if data is None:
        raise ValueError(f"critic returned unparseable JSON:\n{raw[:500]}")

    findings = [
        GuidelinesFinding(
            severity=f.get("severity", "info"),
            category=f.get("category", "unknown"),
            evidence=f.get("evidence", ""),
        )
        for f in data.get("findings", [])
        if isinstance(f, dict)
    ]
    raw_citations = data.get("citations", []) or []
    citations = [
        c.get("text") or c.get("evidence") or c.get("chunk_id") or str(c)
        if isinstance(c, dict) else str(c)
        for c in raw_citations
    ]
    return GuidelinesCriticOutput(
        score=int(data.get("score", 5)),
        findings=findings,
        citations=citations,
    )


def _extract_json(raw: str) -> dict | None:
    """Try several recovery strategies to pull a JSON object out of raw model output."""
    text = raw.strip()
    text = re.sub(r"^```(?:json)?\s*", "", text)
    text = re.sub(r"\s*```$", "", text)

    attempts: list[str] = [text]

    # Extract the largest balanced {...} block (handles prose before/after).
    block = _largest_brace_block(text)
    if block and block != text:
        attempts.append(block)

    # Neutralise invalid backslash escapes in the candidate(s).
    fixed = [_fix_bad_escapes(a) for a in attempts]
    attempts.extend(a for a in fixed if a not in attempts)

    for candidate in attempts:
        try:
            return json.loads(candidate)
        except json.JSONDecodeError:
            continue
    return None


def _largest_brace_block(text: str) -> str | None:
    """Return the substring from the first '{' to its matching '}', ignoring braces in strings."""
    start = text.find("{")
    if start == -1:
        return None
    depth = 0
    in_string = False
    escape = False
    for i in range(start, len(text)):
        ch = text[i]
        if escape:
            escape = False
            continue
        if ch == "\\":
            escape = True
            continue
        if ch == '"':
            in_string = not in_string
            continue
        if in_string:
            continue
        if ch == "{":
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0:
                return text[start : i + 1]
    return None


def _fix_bad_escapes(text: str) -> str:
    """Double-up backslashes that aren't part of a valid JSON escape sequence."""
    return re.sub(r'\\(?!["\\/bfnrtu])', r"\\\\", text)


